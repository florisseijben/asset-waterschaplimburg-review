  function escapeHtml(value) {
    return String(value || "")
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;")
      .replace(/"/g, "&quot;")
      .replace(/'/g, "&#39;");
  }

  function renderParagraphs(items) {
    return items.map(function (item) {
      return "<p>" + escapeHtml(item) + "</p>";
    }).join("");
  }

  function renderList(items) {
    return "<ol>" + items.map(function (item) {
      return "<li>" + escapeHtml(item) + "</li>";
    }).join("") + "</ol>";
  }

  function renderSection(node, section) {
    if (!node || !section) {
      return;
    }

    node.innerHTML =
      '<p class="eyebrow">' + escapeHtml(section.eyebrow) + "</p>" +
      "<h2>" + escapeHtml(section.title) + "</h2>" +
      (section.body ? renderParagraphs(section.body) : "") +
      (section.list ? renderList(section.list) : "");
  }

  function init() {
    var scope = document.body.getAttribute("data-meetprotocol-scope") || "watergangen";
    var sections = scopedSections[scope] || scopedSections.watergangen;
    var nodes = document.querySelectorAll("[data-meetprotocol-section]");

    Array.prototype.forEach.call(nodes, function (node) {
      renderSection(node, sections[node.getAttribute("data-meetprotocol-section")]);
    });
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }
